package com.itwell.iaskmobileclient;

public class IASKconsigment {
	private Long id;
	private String exporterNumber;
	private Long uid;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	
	public String getExporterNumber() {
		return exporterNumber;
	}
	public void setExporterNumber(String exporterNumber) {
		this.exporterNumber = exporterNumber;
	}
	
	public Long getUid() {
		return uid;
	}
	public void setUid(Long uid) {
		this.uid = uid;
	}
}
